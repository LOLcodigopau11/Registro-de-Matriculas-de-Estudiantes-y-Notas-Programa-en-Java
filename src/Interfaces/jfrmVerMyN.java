    public void mostrarTodo(String Valor){
        String sql = Valor.isEmpty()
            ? "SELECT m.num_matricula, m.periodo, m.mes_inicio, m.fecha, m.turno, m.cod_alumno, " +
            "CONCAT(a.nombre, ' ', a.apellido) AS nombre_alumno, " +
            "c.curso, c.tipo, n.num_nota, n.nota, " +
            "CONCAT(e.nombre, ' ', e.apellido) AS nombre_empleado " +
            "FROM tb_matricula m " +
            "INNER JOIN tb_alumno a ON m.cod_alumno = a.cod_alumno " +
            "INNER JOIN tb_curso c ON m.cod_curso = c.cod_curso " +
            "INNER JOIN tb_empleado e ON m.cod_emp = e.cod_emp " +
            "LEFT JOIN tb_nota n ON m.num_matricula = n.num_matricula " +
            "ORDER BY m.num_matricula ASC"
            : "SELECT m.num_matricula, m.periodo, m.mes_inicio, m.fecha, m.turno, m.cod_alumno, " +
            "CONCAT(a.nombre, ' ', a.apellido), " +
            "c.curso, c.tipo, n.num_nota, n.nota, " +
            "CONCAT(e.nombre, ' ', e.apellido) AS nombre_empleado " +
            "FROM tb_matricula m " +
            "INNER JOIN tb_alumno a ON m.cod_alumno = a.cod_alumno " +
            "INNER JOIN tb_curso c ON m.cod_curso = c.cod_curso " +
            "INNER JOIN tb_empleado e ON m.cod_emp = e.cod_emp " +
            "LEFT JOIN tb_nota n ON m.num_matricula = n.num_matricula " +
            "WHERE m.num_matricula LIKE '%" + Valor + "%' OR " +
            "m.mes_inicio LIKE '%" + Valor + "%' OR " +
            "m.periodo LIKE '%" + Valor + "%' OR " +
            "m.cod_alumno LIKE '%" + Valor + "%' OR " +
            "CONCAT(a.nombre, ' ', a.apellido) LIKE '%" + Valor + "%' OR " +
            "CONCAT(e.nombre, ' ', e.apellido) LIKE '%" + Valor + "%' OR " +
            "m.fecha LIKE '%" + Valor + "%' OR " +
            "m.turno LIKE '%" + Valor + "%' OR " +
            "c.curso LIKE '%" + Valor + "%' OR " +
            "m.cod_curso LIKE '%" + Valor + "%' OR " +
            "m.cod_emp LIKE '%" + Valor + "%'";

        try (Connection conn = ConexionBD.conectarBD();PreparedStatement pst = conn.prepareStatement(sql, ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
               ResultSet rs = pst.executeQuery()) {

            // Obtener la cantidad total de filas en el ResultSet
            rs.last();
            int rowCount = rs.getRow();
            rs.beforeFirst();

            DefaultTableModel modeloM = new DefaultTableModel();
            modeloM.addColumn("N°");
            modeloM.addColumn("Periodo");
            modeloM.addColumn("Inicio");
            modeloM.addColumn("Fecha");
            modeloM.addColumn("Turno");
            modeloM.addColumn("C-A");
            modeloM.addColumn("Alumno");
            modeloM.addColumn("Curso");
            modeloM.addColumn("Tipo");
            modeloM.addColumn("N° n");
            modeloM.addColumn("Nota");
            modeloM.addColumn("Docente");
            jtableMatriculasAll.setModel(modeloM);

          // Ajustar el ancho de las columnas
          jtableMatriculasAll.getColumnModel().getColumn(0).setPreferredWidth(8); // Numero Matricula
          jtableMatriculasAll.getColumnModel().getColumn(1).setPreferredWidth(35); // Periodo
          jtableMatriculasAll.getColumnModel().getColumn(2).setPreferredWidth(30); // Mes Inicio
          jtableMatriculasAll.getColumnModel().getColumn(3).setPreferredWidth(35); // Fecha
          jtableMatriculasAll.getColumnModel().getColumn(4).setPreferredWidth(30); // Turno
          jtableMatriculasAll.getColumnModel().getColumn(5).setPreferredWidth(20); // cod_alumno
          jtableMatriculasAll.getColumnModel().getColumn(6).setPreferredWidth(100); // Alumno
          jtableMatriculasAll.getColumnModel().getColumn(7).setPreferredWidth(100); // Curso
          jtableMatriculasAll.getColumnModel().getColumn(8).setPreferredWidth(20); // tipo
          jtableMatriculasAll.getColumnModel().getColumn(9).setPreferredWidth(10); // num nota
          jtableMatriculasAll.getColumnModel().getColumn(10).setPreferredWidth(20); // nota
          jtableMatriculasAll.getColumnModel().getColumn(11).setPreferredWidth(100); // Docente

          // Hacer la tabla no editable
          jtableMatriculasAll.setDefaultEditor(Object.class, null);

          while (rs.next()) {
            Object[] fila = new Object[]{
                rs.getString(1), //num_matricula
                rs.getString(2), //periodo
                rs.getString(3), //mes_inicio
                rs.getString(4), //fecha
                rs.getString(5), //turno
                rs.getString(6), //cod_alumno
                rs.getString(7), // nombre completo del alumno
                rs.getString(8), //curso
                rs.getString(9), //tipo
                rs.getString(10), //num_nota
                rs.getString(11), //nota
                rs.getString(12) // nombre completo del docente
            };
            modeloM.addRow(fila);
        }
          // Mostrar la cantidad total de registros
        lblTotalRegistros.setText("Total de registros: " + rowCount);
        } catch (SQLException ex) {
            Logger.getLogger(jfrmMatricula.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
    
